# Принимаем логин от пользователя
login = input()
# Принимаем пароль от пользователя
password = input()

# Проверяем, является ли пользователь администратором
if login == "admin":
    # Если пользователь администратор, проверяем пароль
    if password == "read":
        # Если пароль "read" - режим чтения
        print("Редактор в режиме чтения")
    elif password == "edit":
        # Если пароль "edit" - режим редактирования
        print("Редактор в режиме редактирования")
    else:
        # Если пароль другой - неправильный пароль
        print("Неправильный пароль")
else:
    # Если пользователь не администратор - обычный пользователь
    print(f"Пользователь {login}")