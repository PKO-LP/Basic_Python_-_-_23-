# Принимаем строку от пользователя и разбиваем на части
numbers = input().split()
# Первое число - количество участников
participants = int(numbers[0])
# Второе число - кратность для отбора
multiple = int(numbers[1])

# Создаем пустой список для победителей
winners = []

# Перебираем числа от 1 до количества участников включительно
for num in range(1, participants + 1):
    # Проверяем, делится ли число на кратность без остатка
    if num % multiple == 0:
        # Если да, добавляем в список победителей
        winners.append(num)

# Выводим список победителей
print(winners)