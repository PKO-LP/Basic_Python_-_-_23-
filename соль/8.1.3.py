# Создаем переменную-счетчик для количества неправильных попыток
incorrect_count = 0

# Принимаем правильный пароль (первая строка)
correct_password = input()

# Бесконечный цикл для приема попыток входа
while True:
    # Принимаем попытку ввода пароля
    attempt = input()

    # Проверяем, правильный ли пароль
    if attempt == correct_password:
        # Если правильный - выводим сообщение и завершаем программу
        print("вошли в почту")
        break
    else:
        # Если неправильный - увеличиваем счетчик неправильных попыток
        incorrect_count += 1
        # Проверяем, является ли текущая попытка третьей неправильной
        if incorrect_count % 3 == 0:
            # Если да - выводим специальное сообщение
            print("три раза уже неправильно, соберись!")
        else:
            # Если нет - выводим обычное сообщение о неправильном пароле
            print("неправильный пароль")