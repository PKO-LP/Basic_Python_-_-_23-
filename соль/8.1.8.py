# Принимаем текст от пользователя
text = input()

# Список слов-паразитов (в нижнем регистре для сравнения)
parasites = ["эээ", "типа", "короче", "ну", "как бы"]

# Разбиваем текст на слова
words = text.lower().split()

# Переменная для отслеживания, найден ли паразит
found = False

# Проверяем каждое слово из текста
for i in range(len(words)):
    # Проверяем текущее слово
    if words[i] in parasites:
        found = True
        break
    # Проверяем, может ли текущее слово быть частью "как бы"
    if words[i] == "как" and i + 1 < len(words) and words[i + 1] == "бы":
        found = True
        break

# Выводим результат
if found:
    print("Слова паразиты обнаружены")
else:
    print("Слов паразитов не обнаружено")