# Принимаем строку с числами и преобразуем в список целых чисел
numbers = list(map(int, input().split()))

# Создаем список из 5 пустых списков для оценок от 1 до 5
result = [[], [], [], [], []]

# Распределяем числа по соответствующим подспискам
for num in numbers:
    # Индекс в списке result = num - 1 (так как оценки от 1 до 5)
    result[num - 1].append(num)

# Выводим результат
print(result)