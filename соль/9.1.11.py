# Принимаем строки и преобразуем в множества
month1 = set(input().split())
month2 = set(input().split())

# Находим людей, которые брали кредит только в одном из месяцев
# Симметрическая разность - элементы, которые есть только в одном из множеств
only_once = month1 ^ month2

# Преобразуем в отсортированный список и выводим
result = sorted(only_once)
print(' '.join(result), end=' ')